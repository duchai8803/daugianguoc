let contract, signer;

async function init() {
	if (typeof window.ethereum === 'undefined') {
		alert('Cần cài đặt MetaMask!');
		return;
	}

	const provider = new ethers.providers.Web3Provider(window.ethereum);
	await provider.send("eth_requestAccounts", []);
	signer = provider.getSigner();
	contract = new ethers.Contract(contractAddress, abi, signer);

	document.getElementById("createForm").addEventListener("submit", async (e) => {
		e.preventDefault();

		const name = document.getElementById("name").value;
		const startPrice = ethers.utils.parseEther(document.getElementById("startPrice").value);
		const reservePrice = ethers.utils.parseEther(document.getElementById("reservePrice").value);
		const duration = parseInt(document.getElementById("duration").value);

		try {
			const tx = await contract.createAuction(startPrice, reservePrice, duration, name);
			await tx.wait();
			alert("✅ Tạo sản phẩm thành công!");
			document.getElementById("createForm").reset();
		} catch (error) {
			console.error(error);
			alert("❌ Lỗi khi tạo sản phẩm.");
		}
	});
}

window.onload = init;
